export function moodAssetsForRole(role = 'family') {
  const isOwner = role === 'owner';
  return MOOD_ASSETS.filter(x => isOwner || x.familySafe);
}

export function findMoodAsset(id, role = 'family') {
  const allowed = moodAssetsForRole(role);
  return allowed.find(x => x.id === id) || allowed[0] || MOOD_ASSETS[0];
}
